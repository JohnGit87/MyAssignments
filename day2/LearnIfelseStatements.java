package week1.day2;

public class LearnIfelseStatements {

	public static void main(String[] args) {
		
		int mark=45;
		
		if(mark<20){
			System.out.println("C");			
		}
		else if(mark<40) {
			System.out.println("B");			
		}
		else if(mark<50) {
			System.out.println("A");			
		}
		else {
			System.out.println("o");
		}				
	}
}
